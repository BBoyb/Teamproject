
public class StarFigures {
	public static void main(String[] args){
		starSeg();
		starSeg();
		lineSeg();
		starSeg();
	}
	public static void starSeg(){
		System.out.println("\n*****\n");
		System.out.println("*****\n");
		System.out.println(" * * \n");
		System.out.println("  *\n");
		System.out.println(" * *\n");
	}
	public static void lineSeg(){
		System.out.println("*****\n");
		System.out.println("*****\n\n");
		System.out.println("  *\n\n  *\n\n  *");
	}
}
